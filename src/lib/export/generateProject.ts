// Generates downloadable source code for a web or mobile app
// based on the components the user placed on the SENTINEL canvas.

export interface ExportNode {
  uid: string;
  name: string;
  itemId: string;
  label: string;
  hint: string;
}

const esc = (s: string) =>
  s.replace(/&/g, "&amp;").replace(/</g, "&lt;").replace(/>/g, "&gt;");

// Render a single canvas node to plain HTML markup.
function renderNode(node: ExportNode): string {
  const label = esc(node.name);
  switch (node.itemId) {
    case "button":
      return `      <button class="ui-button">${label}</button>`;
    case "input":
      return `      <input class="ui-input" placeholder="${label}" />`;
    case "text":
      return `      <p class="ui-text">${label}</p>`;
    case "toggle":
      return `      <label class="ui-toggle"><input type="checkbox" /> ${label}</label>`;
    case "image":
      return `      <div class="ui-image">${label}</div>`;
    case "list":
      return `      <ul class="ui-list"><li>${label} item 1</li><li>${label} item 2</li></ul>`;
    case "grid":
      return `      <div class="ui-grid"><span>${label}</span><span>${label}</span></div>`;
    case "container":
    default:
      return `      <div class="ui-container">${label}</div>`;
  }
}

function renderBody(nodes: ExportNode[]): string {
  if (nodes.length === 0) {
    return `      <p class="ui-text">Your app is empty. Add components in SENTINEL.</p>`;
  }
  return nodes.map(renderNode).join("\n");
}

